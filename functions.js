// Schrijf een functie genaamd getGrades, die een array met de cijfers 8, 8.5, 6 en 7 teruggeeft.
// ---- Verwachte uitkomst bij het aanroepen van de functie: [8, 8.5, 6, 7]

function equalArrays(a1, a2) {
  if (a1.length !== a2.length) {
    return false;
  } else {
    for (let i = 0; i < a1.length; i++) {
      if (a1[i] !== a2[i]) {
        return false;
      }
    }
  }
  return true;
}

function grades() {
  return [8, 8.5, 6, 7];
}
console.assert(equalArrays(grades(), [8, 8.5, 6, 7]), "grades array is not correct");

